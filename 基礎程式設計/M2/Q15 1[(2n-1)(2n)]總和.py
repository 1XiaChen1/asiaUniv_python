# 問題描述： 讓使用者輸入一正整數 n，計算  1/(1∗2)+1/(3∗4)+1/(5∗6)+…+1/[(2n−1)∗(2n)] 並輸出結果。
# 輸入說明： 輸入一正整數 n。
# 輸出說明： 計算  1/(1∗2)+1/(3∗4)+1/(5∗6)+…+1/[(2n−1)∗(2n)] 並輸出結果(double)，最後必須有換行字元。
n = int(input("請輸入一個正整數："))
result = 0.0                                # 初始化結果變數
for i in range(1, n + 1):                   # 計算 1/(1*2) + 1/(3*4) + 1/(5*6) + ... + 1/[(2n-1)*(2n)]
    denominator = (2 * i - 1) * (2 * i)
    result += 1 / denominator
print (result,"\n")                              # 輸出結果，保留小數點後六位